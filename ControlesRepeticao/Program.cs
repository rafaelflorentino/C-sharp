// While
int inicializacao= 0; // inicializacao 

while(inicializacao<= 3) // condicao 
{ 
    Console.WriteLine("instrução do laço: "+ inicializacao); 
    inicializacao++; // incrementos 
}

// Do While
inicializacao = 0; // inicialização 

do { 
    Console.WriteLine("instrução laço: "+ inicializacao); 
    inicializacao++; // incremento 
} while(inicializacao<= 3); // condição

// For
for (int inicio = 0; inicio <= 10; inicio++) 
{ 
    Console.WriteLine("Início: " + inicio); 
}